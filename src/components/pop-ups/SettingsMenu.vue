<template>
  <v-dialog activator="parent">
    <v-card>
      <v-card-title> Settings </v-card-title>
      <v-card-text>
        <v-switch
          v-model="isDarkMode"
          label="Dark Mode"
          color="secondary"
          inset
          hide-details
          @change="toggleTheme"
        ></v-switch>
        <v-btn
          @click="deleteCache"
        >
          Delete Cache
        </v-btn>
      </v-card-text>
    </v-card>
  </v-dialog>
</template>

<script>
import { defineComponent } from "vue";
import { useTheme } from "vuetify";

export default defineComponent({
  name: "SettingsMenu",

  setup() {
    const theme = useTheme();

    return {
      theme,
      toggleTheme: () =>
        (theme.global.name.value = theme.global.current.value.dark
          ? "light"
          : "dark"),
    };
  },

  data() {
    return {
      isDarkMode: true,
    };
  },

  methods: {
    deleteCache() {
      this.$emit("delete-cache");
    }
  }
});
</script>
